package day2;

public class Greater {

	public static void main(String[] args) {
		int i=0;
		int j=10;
		
		if (i>j)
		{
			System.out.println("First number is greater");
		}
		else if (i==j)
		{
			System.out.println("Both numbers are equal");
		}
		else 
		{
			System.out.println("Second number is greater");
		}
		
	}

}
